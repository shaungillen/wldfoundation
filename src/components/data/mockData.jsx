// Selector/helper functions
export function getArtists() {
  return artists;
}

export function getArtist(id) {
  return artists.find(artist => artist.id === id) || null;
}

export function getArtistsByFilter({ featured, nationality, mediumFocus } = {}) {
  let results = [...artists];
  
  if (featured !== undefined) {
    results = results.filter(a => a.featured === featured);
  }
  
  if (nationality) {
    results = results.filter(a => a.nationality?.toLowerCase().includes(nationality.toLowerCase()));
  }
  
  if (mediumFocus) {
    results = results.filter(a => a.medium_focus?.includes(mediumFocus));
  }
  
  return results;
}

export function getArtworks({ artistId, q, medium, status, category } = {}) {
  let results = [...artworks];
  
  if (artistId) {
    results = results.filter(a => a.artist_id === artistId);
  }
  
  if (q) {
    const query = q.toLowerCase();
    results = results.filter(a => 
      a.title?.toLowerCase().includes(query) ||
      a.artist_name?.toLowerCase().includes(query) ||
      a.themes?.some(t => t.toLowerCase().includes(query)) ||
      a.medium?.toLowerCase().includes(query)
    );
  }
  
  if (medium) {
    results = results.filter(a => a.medium?.toLowerCase().includes(medium.toLowerCase()));
  }
  
  if (status) {
    results = results.filter(a => a.status === status);
  }
  
  if (category) {
    results = results.filter(a => a.category === category);
  }
  
  return results;
}

export function getArtwork(id) {
  return artworks.find(artwork => artwork.id === id) || null;
}

export function getArtworksByRoomId(roomId) {
  const roomCategoryMap = {
    'main-gallery': 'painting',
    'study-room': 'works_on_paper',
    'sculpture-court': 'sculpture',
  };
  
  const category = roomCategoryMap[roomId];
  if (!category) return [];
  
  return artworks.filter(a => a.status === 'on_view' && a.category === category);
}

